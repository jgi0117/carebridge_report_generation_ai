from __future__ import annotations


SYSTEM_PROMPT = """
당신은 고령자 케어 서비스의 월간 감정 리포트를 작성하는 AI입니다.
입력 데이터는 얼굴 이미지 기반 감정 분류 모델이 산출한 확률의 월별 집계입니다.
확률은 진단이 아니라 관찰 보조 지표로만 해석해야 합니다.
과장된 의학적 판단을 피하고, 보호자와 케어 매니저가 이해하기 쉬운 한국어로 작성하세요.
반드시 모든 문장을 완성된 형태로 마무리하세요.
리포트 마지막에는 반드시 '종합 의견:' 섹션을 작성하고 그 섹션으로 종료하세요.
""".strip()


def build_monthly_report_prompt(month_summary: dict, trend_context: list[dict]) -> str:
    """월별 집계 데이터를 리포트 생성용 프롬프트로 변환합니다."""
    return f"""
아래 데이터를 바탕으로 월간 감정 리포트를 작성해줘.

[작성 형식]
## 월간 감정 리포트
- 관찰 기간:
- 주요 감정 흐름:
- 주의해서 볼 변화:
- 케어 제안:
- 한 줄 요약:
종합 의견:

[선택 월 집계]
{month_summary}

[최근 월별 흐름]
{trend_context}

[주의사항]
- 숫자는 필요한 곳에만 간단히 언급해줘.
- 확률 값은 소수로 쓰지 말고, 입력에 있는 '약 x%' 형식을 그대로 사용해줘.
- "확정", "진단", "우울증" 같은 단정적 표현은 피하고 관찰 기반으로 말해줘.
- 케어 제안은 실천 가능한 행동 2~3개로 제한해줘.
- 모든 문장은 완성된 문장으로 끝내고, 문장 중간에서 끊지 마.
- 리포트의 마지막 문단은 반드시 '종합 의견:' 섹션이어야 해.
""".strip()
